	 __uint32_t dcr, state = 0x0;
	 __uint32_t   CmapData[] = {
		0xCCCCCC, 0xaa0000, 0xaaaaaa, 
		0x06aaaa, 0x000600, 0x000006,
		0x070000, 0x080707,
		0x05AAAA, 0xAA05AA, 0xAAAA05,
		0x06AAAA, 0x000600, 0x000006,
		0x070000, 0x080707, 0xfafafa,
		0xfafafa, 0xfafafa, 0xfafafa,
		0xfafafa, 0xfafafa, 0xfafafa,
		0xfafafa, 0xfafafa, 0xfafafa,
		0xfafafa, 0xfafafa, 0xfafafa,
	};

	MGRAS_GFX_CHECK();

	CurSramAddr =  0x500;
	msg_printf(DBG, "mgras_VC3CursorMode \n");

	msg_printf(DBG, "Disable Video Timing \n");
#if 0
	dcr = 0x7880;
#endif
	dcr = 0x6;
	dcr &= ~VC3_VIDEO_ENAB;
/* mgras_vc3SetReg(mgbase,VC3_DISPLAY_CONTROL, dcr); */


        mgras_xmapSetAddr(mgbase,0x4); /* Top Scan Reg */
        mgras_xmapSetDIBdata(mgbase, 0xc7); /* Y value of the Top Scan = 200 */

        mgras_xmapSetConfig(mgbase, 0x0d5000); 	/* Cmap for Cursor is at 0x00 */
	msg_printf(DBG, "Top Scan, PP1 config regs set\n");

	/* turn off the cursor */
	mgras_VC3CursorDisable();

	switch(mode) {
		case CROSS_HAIR:
			msg_printf(DBG, "CROSS_HAIR cursor  loading \n");
			state |=  VC3_CURS_CROSSHAIR ;
			msg_printf(DBG, "Cursor loaded \n");
			break;
		case DIAG_CURS:
			msg_printf(DBG, "CMAP loading \n");
			mgras_LoadCmap(mgbase,0x0, (char*)CmapData, 10) ;
			msg_printf(DBG, "CMAP loaded \n");

			msg_printf(DBG, "DIAG_CURS cursor  loading \n");
			mgras_LoadVC3SRAM(CurSramAddr, ( ushort_t *) diag_curs, sizeof(diag_curs)/sizeof(diag_curs[0]), DATA16);
			state &=  ~(VC3_CURS_GLYPH_64 | VC3_CURS_CROSSHAIR);
			msg_printf(DBG, "DIAG_CURS Cursor loaded \n");
			break;
		case GLYPH_64:
			msg_printf(DBG, "GLYPH_64 cursor  loading \n");
			mgras_LoadVC3SRAM(CurSramAddr, ( ushort_t *) sgi64_bits, sizeof(sgi64_bits)/sizeof(sgi64_bits[0]), DATA16);
			state &=  ~(VC3_CURS_GLYPH_64 | VC3_CURS_CROSSHAIR);
			state |=  VC3_CURS_GLYPH_64;
			msg_printf(DBG, "GLYPH_64 Cursor loaded \n");
			break;
		case GLYPH_32:
		default:
			msg_printf(DBG, "== GLYPH_32 cursor  loading \n");
			mgras_LoadVC3SRAM(CurSramAddr, ( ushort_t *) arrow_32x32, sizeof(arrow_32x32)/sizeof(arrow_32x32[0]), DATA16);
			state &=  ~(VC3_CURS_GLYPH_64 | VC3_CURS_CROSSHAIR);
			msg_printf(DBG, "** GLYPH_32 Cursor loaded \n");
			break;
	}

	state = 0x3; 	/* HACK */
	/* select the cursor size */
	mgras_vc3SetReg(mgbase, VC3_CURS_CONTROL, state);

	/* Set the cursor entry po__uint32_ter */
	mgras_vc3SetReg(mgbase, VC3_CURS_EP, CurSramAddr);

	mgras_vc3SetReg(mgbase, VC3_SCAN_LENGTH, (200<<5));  /* Total Number of Visibile Pixels in a Scan Line :  Follows Timing Table */

        mgras_vc3SetReg (mgbase, VC3_CURS_X_LOC, 0x27);	/* Cannot be Zero */
        mgras_vc3SetReg (mgbase, VC3_CURS_Y_LOC, 0x27);	/* Cannot be Zero */


	msg_printf(DBG, "Enable cursor\n");
	/* Turn the cursor back on */
	mgras_VC3CursorEnable();

	return 0;
