__uint32_t _Verify_Sram_Load(__uint32_t SramAddr,  ushort_t *Exp, __uint32_t length)
{
         ushort_t addr;
	 ushort_t index    = 0;
         __uint32_t errors   = 0;
         ushort_t mask     = 0xffff;
         ushort_t rcv      = 0xdead;

	msg_printf(DBG, "_Verify_Sram_Load\n");
	for(index=0, addr = SramAddr; index<length ; index++, addr++) {
		mgras_GetVC3SRAM(addr,  &rcv,  1, DATA16);
		compare_report6("VC3 SRAM Load Verify", addr, (*(Exp + index)), rcv, mask, &errors);
	}
	msg_printf(DBG, "_Verify_Sram_Load  Done\n");
	return (errors);
}

