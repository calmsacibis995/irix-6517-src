void 
mgras_LoadVC3SRAM( __uint32_t addr,  ushort_t *data,  __uint32_t length,  __uint32_t DataWidth)
{
         __uint32_t i, Count;
	__uint64_t	val64;


	if (_mgras_dcbdma_enabled) {
#if HQ4
#define VC3_DCB_DEV 8
		mgras_vc3SetReg(mgbase,VC3_RAM_ADDR, addr);
		mgras_BFIFOWAIT(mgbase,HQ3_BFIFO_MAX);
		mgbase->dcb_dma_con = ((VC3_DCB_DEV << 6) | (3 << 3) | (1 << 2) | 2) ;
		_mgras_create_buffer();
		_fill_buffers((uchar_t *)dcbdma_buffer_ptr,(uchar_t *)data,length*2,length*2); 
		_mgras_rttexture_setup(TEX_DCB_IN, 0, length*2, dcbdma_buffer_ptr);
#endif
	}
	else {

	
		(Count = DataWidth ? length/4 : length) ;

           mgras_vc3SetReg(mgbase,VC3_RAM_ADDR, addr);
           mgras_BFIFOWAIT(mgbase,HQ3_BFIFO_MAX);
           for (i=0 ;i<Count; i++) {
	     if (!DataWidth) {
               	mgras_vc3SetRam(mgbase,*(data+i));
	     } else  {
		val64 = (*((__uint64_t *)data+i));
               	mgras_vc3SetRam64(mgbase, val64);
	     }

              if ((i & 0x3f) == 0x3f) {
                 mgras_BFIFOWAIT(mgbase,HQ3_BFIFO_MAX);
              }
            }
         } /* else _mgras_dcbdma_enabled */
}


void 
mgras_GetVC3SRAM( __uint32_t addr,  ushort_t *data,  __uint32_t length,  __uint32_t DataWidth)
{
         __uint32_t i, Count;
	 ushort_t  *RdPtr;
	__uint64_t	*RdPtr64;


	(Count = DataWidth ? length/4 : length) ;
	if (!DataWidth) {
		RdPtr = data;
	} else {
		RdPtr64 = (__uint64_t *)data;
	}
        mgras_vc3SetReg(mgbase,VC3_RAM_ADDR, addr);

        mgras_BFIFOWAIT(mgbase,HQ3_BFIFO_MAX);
        for (i=0;i < Count;i++)
        {
		if (!DataWidth) {
                	mgras_vc3GetRam(mgbase, (*RdPtr));
			RdPtr++;
		} else {
                	mgras_vc3GetRam64(mgbase, (*RdPtr64));
			RdPtr64++;
		}
                if ((i & 0x3f) == 0x3f)
                {
                        mgras_BFIFOWAIT(mgbase,HQ3_BFIFO_MAX);
                }
        }
}


__uint32_t
mgras_VC3Reset(void)
{
	 __uint32_t Bit, errors = 0;
	 __uint32_t state = 0xdead;

	MGRAS_GFX_CHECK();

	/*
 	 * Board Version Register 0 and Board Config reg 0 are the SAME.
 	 * The following statments will reset VC3 and release the reset on VC3
	 */
	mgras_MaxPLL_NoTiming();

	mgras_vc3GetReg(mgbase,VC3_CONFIG, state);
	msg_printf(DBG, "=======   VC3 Config Info ====\n ");
	for(Bit=0; Bit < 7; Bit++, state >>=1) {
		switch(Bit) {
			case 0:
				if (state & 0x1) {
					msg_printf(DBG, "VC3 - Normal Operation :  %d\t" , state & 0x1);
				} else {
					msg_printf(DBG, "VC3 - SoftReset:  %d\t" , state & 0x1);
				}
				break;
			case 1:
				if (state & 0x1) {
					msg_printf(DBG, "Fast Mode, Monitor Pixel >= 85Mhz:  %d\n" ,state & 0x1);
				} else {
					msg_printf(DBG, "Slow Mode, Monitor Pixel < 85Mhz:  %d\n" ,state & 0x1);
				}
				break;
			case 2:
				if (state & 0x1) {
					msg_printf(ERR, "\nCursor Error Occurred:   %d  \n" ,state & 0x1);
					errors++;
				} else {
					msg_printf(DBG, "Cursor Flag :  %d  \n" ,state & 0x1);
				}
				break;
			case 3:
				if (state & 0x1) {
					msg_printf(INFO, "\nDID1 Fifo Underflow :  %d  \n" ,state & 0x1);
				} else {
					msg_printf(DBG, "DID1 Fifo Underflow Flag Cleared : %d\n" ,state & 0x1);
				}
				break;
			case 4:
				if (state & 0x1) {
					msg_printf(INFO, "\nDID2 Fifo Underflow :  %d  \n" ,state & 0x1);
				} else {
					msg_printf(DBG, "DID2 Fifo Underflow Flag Cleared : %d\n" ,state & 0x1);
				}
				break;
			case 5:
				if (state & 0x1) {
					msg_printf(ERR, "\nVTG Fifo Underflow :  %d  \n" ,state & 0x1);
					errors++;
				} else {
					msg_printf(DBG, "VTG Fifo Underflow Flag Cleared : %d\t" ,state & 0x1);
				}
				break;
			case 6:
				if (state & 0x1) {
					msg_printf(ERR, "\nVC3 Revision  Wrong :  %d should be 0\n" ,state & 0x3); 
					errors++;
				} else {
					msg_printf(DBG, "VC3 Revision 	:  %d\n" ,state & 0x3); 
				}
				break;
		}
	}
	msg_printf(DBG, "=======   VC3 Config Info ====\n ");
	
	return (errors);
}

