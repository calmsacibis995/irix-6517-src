extern void MgrasCalcScreenSize(struct mgras_timing_info *tinfo, 
				unsigned short *w, unsigned short *h)
{
    int x, y;

    x = tinfo->w;
    if (tinfo->flag & MGRAS_FULLSCREEN_STEREO) {
	/* utter hack - the info is not present in the timing table */
	y = 1024;
    } else if ((tinfo->num_fields == 0) ||
	       (tinfo->flag & MGRAS_INTERLACED)) {
	y = tinfo->h;
    } else {
	y = tinfo->h / tinfo->num_fields;
    }
    *w = x;
    *h = y;
}
