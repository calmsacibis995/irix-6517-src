/* ARGSUSED */
size_t
__nregerror(int errcode, const regex_t *preg, char *errbuf, size_t errbuf_size)
{
	sgi_regex_t	oregex;
	sgi_regex_t	*oregexp;
	unsigned char *cp1, *cp2;
	int i;

	if(preg) {
		/*
		 * Convert the MIPS ABI/XPG4 version of the regex_t 
		 * structure into the version used for SGI's implementation 
		 * before calling __regerror().
		 */
	
		/* new to old conversion */
		oregexp = &oregex;
		oregexp->re_nsub = preg->re_nsub;
			
		/*
		 * Store the re_pad[0] element from the regex_t structure
		 * into the re_magic struct element of the sgi_regex_t 
		 * structure.
		 */
		cp1 = (unsigned char *)&preg->re_pad;
		cp2 = (unsigned char *)&oregexp->re_magic;
		for(i=0; i<sizeof(oregexp->re_magic); i++)
			*cp2++ = *cp1++;
	
		oregexp->re_endp = (const char *)preg->re_pad[1];
	        oregexp->re_g = (struct re_guts *)preg->re_pad[2];
	}
	/*
	 * add argument to indicate old or new version of regerror():
	 *
	 *	zero means old; one means new
	 */
	return __regerror(errcode, oregexp, errbuf, errbuf_size, (int)1);
}

/* ARGSUSED */
static size_t
__regerror(int errcode, const sgi_regex_t *preg, char *errbuf, size_t errbuf_size, int new)
{
	register struct rerr const *r;
	register size_t len;
	register int target;
	register char *s;
	char convbuf[50];

	target = errcode &~ REG_ITOA;
	if (errcode == REG_ATOI)
	        s = regatoi(preg, convbuf, new);
	else {
		if(new) {
			for (r = rerrs; r->code != 0; r++)
				if (r->code == target)
					break;
		} else {
			for (r = rerrs; r->ocode != 0; r++)
				if (r->ocode == target)
					break;
		}
		if (errcode&REG_ITOA) {
		        if (r->code != 0)
			        (void) strcpy(convbuf, r->name);

			sprintf(convbuf, "REG_0x%x", target);
			assert(strlen(convbuf) < sizeof(convbuf));
			s = convbuf;
		} else
		    s = (char *) gettxt ( r->msg_id, r->dflt );
	   
	}

	len = strlen(s) + 1;
	if (errbuf_size > 0) {
		if (errbuf_size > len)
			(void) strcpy(errbuf, s);
		else {
			(void) strncpy(errbuf, s, errbuf_size-1);
			errbuf[errbuf_size-1] = '\0';
		}
	}
   
   	return(len);
}

/*
 - regatoi - internal routine to implement REG_ATOI
 == static char *regatoi(const sgi_regex_t *preg, char *localbuf);
 */
static char *
regatoi(const sgi_regex_t *preg, char *localbuf, int new)
{
	register struct rerr const *r;

	for (r = rerrs; r->code != 0; r++)
		if (strcmp(r->name, preg->re_endp) == 0)
			break;
	if (r->code == 0)
		return("0");

	if ( new )
	    sprintf(localbuf, "%d", r->code);
	else
	    sprintf(localbuf, "%d", r->ocode);
	return(localbuf);
}
