/*
 - regerror - the interface to error numbers
 = extern size_t regerror(int, const regex_t *, char *, size_t);
 */

static size_t __regerror(int, const sgi_regex_t *, char *, size_t, int);

/* ARGSUSED */
size_t
regerror(int errcode, const sgi_regex_t *preg, char *errbuf, size_t errbuf_size)
{
	/*
	 * This is the backward compatible version of regerror().  This will
	 * always get called from an application which was link-loaded using
	 * a previous version of libc.so.
	 *
	 * add argument to indicate old or new version of regerror():
	 *
	 *	zero means old; one means new
	 */
	return __regerror(errcode, preg, errbuf, errbuf_size, (int)0);
}

