/*
 *	tmpfile - return a pointer to an update file that can be
 *		used for scratch. The file will automatically
 *		go away if the program using it terminates.
 */
#ifdef __STDC__
	#pragma weak tmpfile64 = tmpfile
	#pragma weak _tmpfile64 = tmpfile
#endif

#include "synonyms.h"
#include "mplib.h"
#include <stdio.h>
#include <string.h>
#include <stdio.h>
#include <stdlib.h>
#include <fcntl.h>
#include <unistd.h>	/* prototype for unlink() */

/*
 * there doesn't appear to be anything in the early LFS spec
 * that says that we can't always give them a large file capable
 * tmp file
 */
static char seed[] = { 'a', 'a', 'a', '\0' };

#define XS "\bXXXXXX"		/* a '\b' character is prepended to this
				* string to avoid conflicts with names
				* generated by tmpnam() */

FILE *
tmpfile(void)
{
	char	tfname[L_tmpnam];
	register FILE	*p;
	register char	*q;
	int		fd;
	LOCKDECLINIT(l, LOCKOPEN);

	(void) strcpy(tfname, P_tmpdir);
	(void) strcat(tfname, seed);
	(void) strcat(tfname, XS);

	q = (char *)seed;
	while(*q == 'z')
		*q++ = 'a';
	if (*q != '\0')
		++*q;

	(void) mktemp(tfname);

	fd = open(tfname, O_RDWR | O_CREAT | O_EXCL, 0666);
	if (fd == -1) {
	    return NULL;
	}
	(void) unlink(tfname);
	p = fdopen(fd, "w+");

	UNLOCKOPEN(l);
	return(p);
}
