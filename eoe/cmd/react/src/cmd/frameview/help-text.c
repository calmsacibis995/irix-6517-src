strcat(help_text, "and has many display features geared towards displaying streams generated\n");
strcat(help_text, "by frame scheduled applications.  By default FrameView will look for events\n");
strcat(help_text, "indicating the beginning of frames generated by the frame scheduler, and\n");
strcat(help_text, "will switch to no frame mode if it can not locate any start of frame\n");
