strcat(help_text, "not logged then certain options that require past information will not be\n");
strcat(help_text, "available, such as viewing a previous frame.  The other action required is\n");
strcat(help_text, "for the user to designate on what cpus the stream is to be gathered.  For\n");
strcat(help_text, "the local machine FrameView knows how many processors are available, but\n");
